#include "winuser.h"

/////////////////////////////////////////////////////////////////////////////

// Test for presence of TabletPC

inline bool IsTabletPC()
{
	static bool bInitialized = false;
	static bool bIsTabletPC = false;

	if (!bInitialized)
		bIsTabletPC = GetSystemMetrics(86 /*SM_TABLETPC*/) !=0;
       
	return bIsTabletPC;
}

